# EX08 - Escreva um programa que leia um valor em metros e exiba convertido em centímetros e milímetros.
# ----------------------------------------------------------
print('======== EXERCÍCIO 8 ========')

met = float(input("Digite aqui um valor em metros, para receber o valor convertido em centímetros, e em milímetros: "))
print(f"O valor {met} de metros para centímetros é {met*100}. Já em milímetros é {met*1000}!")