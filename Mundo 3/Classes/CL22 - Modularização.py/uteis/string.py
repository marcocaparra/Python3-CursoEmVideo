def textoParaMaiusculo(texto):
    return texto.upper()

def textoParaMinusculo(texto):
    return texto.lower()

def textoParaCapitalizado(texto):
    return texto.capitalize()

def textoParaLista(texto):
    return texto.split()

def listaParaTexto(lista):
    return ' '.join(lista)

def textoParaTamanho(texto):
    return len(texto)